package LoopsAndFunctions;

public class LargestNumUseConditionalOper {
    public static void main(String[] args) {
       //Java Program To Find Smallest Number Using Conditional Operator

       int a=10,b=20,c=30;

       System.out.println("Largest Number is "+(a>b?(a>c?a:c):(b>c?b:c)));
    }
}
