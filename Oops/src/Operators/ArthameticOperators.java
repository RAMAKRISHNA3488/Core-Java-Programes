package Operators;

public class ArthameticOperators {
	public static void main(String[] args) {
		int a=20;
		int b=10;
		//Addition Operators
		int add=a+b;
		int sub=a-b;
		int mul=a*b;
		float div=a/b;
		int rem=a%b;
		System.out.println("The Addition: "+a+" + "+b+" = "+add);
		System.out.println("The Subtraction: "+a+" - "+b+" = "+sub);
		System.out.println("The Multiplication: "+a+" * "+b+" = "+mul);
		System.out.println("The Division: "+a+" / "+b+" = "+div);
		System.out.println("The Modulo Division: "+a+" % "+b+" = "+rem);
		
	}

}
