package Operators;

class RelationalOperators {
	// main function
	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		int c = 5;
		// Comparison operators
		System.out.println("a > b: " + (a > b));
		System.out.println("a < b: " + (a < b));
		System.out.println("a >= b: " + (a >= b));
		System.out.println("a <= b: " + (a <= b));
		System.out.println("a == c: " + (a == c));
		System.out.println("a != c: " + (a != c));
	}
}
