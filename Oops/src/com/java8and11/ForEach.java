package com.java8and11;

public class ForEach {
	public static void main(String[] args) {
		int a[] = { 1, 44, 55, 667, 87, 1000 };
		for (int i : a) {
			System.out.println(i);
		}
	}

}
/*
 * Use the for-each loop to iterate through collections and arrays.this is
 * feature that has been available since java 5.
 */
